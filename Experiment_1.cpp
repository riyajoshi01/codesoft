#include <iostream>
using namespace std;
int main() {
    float a,b,add,subtract,multiply,divide;
    cout<<"Enter first number : ";
    cin>>a;
    cout<<"Enter second number : ";
    cin>>b;
    add=a+b;
    subtract=a-b;
    multiply=a*b;
    divide=a/b;
    cout<<"Sum is : "<<add<<"\n";
    cout<<"substract is : "<<subtract<<"\n";
    cout<<"Mutiplication is : "<<multiply<<"\n";
    cout<<"Division  is : "<<divide<<"\n";
}